#include "libft.h"

int	ft_putchar(int c)
{
	unsigned char out[2];

	if (c < 0x80)
		return (write(1, &c, 1));
	else
	{
		out[0] = 0xC0 | ((c >> 6) & 0xF);
		out[1] = 0x80 | (c & 0x3F);
		return (write(1, out, 2));
	}
}
